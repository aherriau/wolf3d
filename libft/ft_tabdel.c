#include "libft.h"

void	ft_tabdel(char **tab)
{
	int		i;

	if (tab != NULL)
	{
		i = 0;
		while (tab[i] != NULL)
		{
			ft_strdel(&(tab[i]));
			i++;
		}
		free(tab);
		tab = NULL;
	}
}
